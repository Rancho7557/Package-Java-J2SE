package javacode;

public class Operator_relation {

	public static void main(String[] args) {
		 
		int a = 10, b = 20;
		
		System.out.println(a == b); // a10 == b20 false
		
		System.out.println(a != b); // a10 not equal b20 true
		
		System.out.println(a > b); // a10 greater then b20 false
		
		System.out.println(a < b); // a10 less then b20 true
		
		System.out.println(a <= b); //a10 less then equal b20 true
		
		System.out.println(a >= b); // a10 greater then equal b20 false

	}

}

package javacode;

public class Logical_Operater {

	public static void main(String[] args) {
		 
		int a = 10, b = 20;
		System.out.println(a == b && a != b);
//Every condition need to true then Run if any one condition false it mean false.
		
		System.out.println(a == b || a != b);
//if in this operator one condition true mean true
		
		System.out.println(!(a > b));
// in this Operator true to false and false to true convert.

	}

}
